fruits = ("apple", "apricot", "banana", "coconut", "lemen", "plum", "pear")

print(fruits)

print("Element count: ", len(fruits))

print("fruits[-1]: ", fruits[-1])
print("fruits[-2]: ", fruits[-2])

subTuple1 = fruits[-4:]

print("\n")
print("Sub Tuple fruits[-4: ] ")
print(subTuple1)

subTuple2 = fruits[-4:-2]

print("\n")
print("Sub Tuple fruits[-4:-2] ")
print(subTuple2)